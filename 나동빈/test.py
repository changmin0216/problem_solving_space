import pandas as pd

# 데이터 생성
data = {
    "NO": list(range(1, 31)),  # 예시 번호 (1~30)
    "전공명": ["전공"] * 30,  # 예시 전공명
    "수강연도": [2017] * 10 + [2018] * 10 + [2020] * 10,  # 연도 분배
    "학기": [1, 2] * 15,  # 학기 1, 2 반복
    "과목명": [f"과목{i}" for i in range(1, 31)],  # 과목명 예시
    "과목유형": ["전공"] * 20 + ["교양기타"] * 10,  # 과목유형 예시
    "취득학점": [3] * 30,  # 취득학점 3 고정
    "성적": ["A+", "B+", "C+", "B", "D+", "C", "A", "B", "C+", "P"] * 3,  # 성적 반복
    "재수강여부": ["N"] * 25 + ["Y"] * 5  # 일부 재수강 포함
}

# 데이터프레임 생성
df = pd.DataFrame(data)

# 엑셀 파일 저장 (국문 양식 유지)
file_path = "수강내역1.xlsx"  # 저장 경로
df.to_excel(file_path, index=False, engine="openpyxl")

print(f"파일이 생성되었습니다: {file_path}")
